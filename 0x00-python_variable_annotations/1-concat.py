#!/usr/bin/env python3
"""
This module provides a function 'concat' that concatenates two strings.

Author: Your Name
"""


def concat(str1: str, str2: str) -> str:
    """
    Concatenates two input strings.

    :param str1: The first input string.
    :param str2: The second input string.
    :return: The concatenated string of 'str1' and 'str2'.
    """
    return str1 + str2
